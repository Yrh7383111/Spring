package Pointcut_Declaration;



public class Account
{
	// Private
	private String name;
	private String level;


	// Public
	public String getName()
	{
		return name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public String getLevel()
	{
		return level;
	}
	
	public void setLevel(String level)
	{
		this.level = level;
	}
}